/// Border radius padronizados do Design System. Não usar números mágicos.
abstract class AppRadius {
  /// Border radius do container de formulário (card).
  static const double formContainer = 16;

  /// Tiles e cards de lista (itens de listagem).
  static const double tile = 12;

  /// Elementos pill (filtros, chips).
